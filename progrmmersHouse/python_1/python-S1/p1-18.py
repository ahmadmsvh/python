print("execute four main operator on two input numbers")

first_number=float(input("enter the first number:\t"))
second_number=float(input("enter the second number:\t"))

sum=first_number+second_number
sub=first_number-second_number
mult=first_number*second_number
div=first_number/second_number

print("%f + %f=%f\t" %(first_number,second_number,sum))
print("%f - %f=%f\t" %(first_number,second_number,sub))
print("%f x %f=%f\t" %(first_number,second_number,mult))
print("%f / %f=%f\t" %(first_number,second_number,div))


